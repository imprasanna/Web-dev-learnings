//Arithmetic operator

const number1 = 1;
const number2 = 2;

//sum
const sum = number1 + number2;
console.log(sum);

//difference
const diff = number1 - number2;
console.log(diff);

//multiply
const mul = number1 * number2;
console.log(mul);

//division
const div = number1 / number2;
console.log(div);

//modulo division
const modulo = number1 % number2;
console.log(modulo);

//Conditional operator
const a = 3;
const b = 2;

console.log(a == b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
console.log(a != b);
